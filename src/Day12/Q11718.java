package Day12;

import java.util.Scanner;

public class Q11718 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        while (sc.hasNext()){
            String str = sc.nextLine(); // next:스 페이스바 이전값을 출력 nextline: 엔터 이전값을 출력
            System.out.println(str);
        }
    }
}
